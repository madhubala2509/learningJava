package javaFeatures.java7Features;
/*
use the prefix 0b or 0B to express integer in binary system
 */
public class binaryLiterals {
    public static void main(String args[]){
        int a = 0b101;
        int b = 0b1111;
        int c = 0B010101;
        int d = -0b101;
        System.out.println(a+" "+b+" "+c+" "+d);
    }
}
