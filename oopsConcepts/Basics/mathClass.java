package oopsConcepts.Basics;

public class mathClass {
    public static void main(String args[]){
        //basic math functions
        System.out.println("Maximum is "+Math.max(2,3));
        System.out.println("Minimum is "+Math.min(2,3));
        System.out.println("Power of"+Math.pow(4,3));
        System.out.println("Square root is"+Math.sqrt(132));

        //log functions
        System.out.println("Log fn"+Math.log(2));
        System.out.println("log func"+Math.log10(50));

        //trigometric functions
        System.out.println("sin func"+Math.sin(55));
        System.out.println("cos func"+Math.cos(66));
        System.out.println("tan func"+Math.tan(55));

        //angular functions
        System.out.println("to degree"+Math.toDegrees(1452));
        System.out.println("to radian"+Math.toRadians(66));
    }
}
